def load_text_embedding(record_dir, llm_model="GPT2", llm_layers=6):
    emb_file = find_embedding_file(record_dir, llm_model, llm_layers)
    if emb_file is None:
        return None, 0
    try:
        obj = torch.load(emb_file, map_location="cpu")
        arr = None
        if isinstance(obj, dict):
            for key in ["embeddings", "embedding", "data", "text_embeddings", "x"]:
                if key in obj:
                    arr = obj[key]
                    break
            if arr is None:
                for v in obj.values():
                    if hasattr(v, "shape"):
                        arr = v
                        break
        else:
            arr = obj
        if arr is None:
            return None, 0
        if hasattr(arr, "detach"):
            arr = arr.detach().cpu().numpy()
        else:
            arr = np.asarray(arr)
        if arr.ndim == 1:
            arr = arr.reshape(1, -1)
        if arr.size == 0:
            return None, 0
        return arr.mean(axis=0).astype(np.float32), arr.shape[0]
    except Exception:
        return None, 0

def summarize_text_meta(record_dir):
    text_path = record_dir / "text.csv"
    if not text_path.exists():
        return dict(txt_n_rows=0.0, txt_missing_frac=np.nan, txt_timespan_hours=np.nan, txt_mean_gap_hours=np.nan)
    try:
        df = pd.read_csv(text_path)
    except Exception:
        return dict(txt_n_rows=0.0, txt_missing_frac=np.nan, txt_timespan_hours=np.nan, txt_mean_gap_hours=np.nan)

    time_col = find_time_col(df)
    if time_col is not None:
        tt = pd.to_datetime(df[time_col], errors="coerce")
    else:
        tt = pd.Series([pd.NaT] * len(df))

    exclude = set(TIME_CANDIDATES + ["record_id"])
    txt_cols = [c for c in df.columns if c not in exclude]
    txt_missing = float(df[txt_cols[0]].isna().mean()) if len(txt_cols) >= 1 else np.nan

    out = {
        "txt_n_rows": float(len(df)),
        "txt_missing_frac": txt_missing,
        "txt_timespan_hours": np.nan,
        "txt_mean_gap_hours": np.nan,
    }
    if tt.notna().sum() >= 2:
        tt2 = tt.sort_values()
        diffs = tt2.diff().dropna().dt.total_seconds() / 3600.0
        out["txt_timespan_hours"] = float((tt.max() - tt.min()).total_seconds() / 3600.0)
        out["txt_mean_gap_hours"] = float(diffs.mean()) if len(diffs) else np.nan
    return out

def load_time_series(record_dir):
    ts_path = record_dir / "time_series.csv"
    if not ts_path.exists():
        return None, None, None
    df = pd.read_csv(ts_path)
    if df.empty:
        return None, None, None

    time_col = find_time_col(df)
    exclude = set([time_col] if time_col is not None else [])
    exclude.update(["record_id"])

    feature_cols = []
    for c in df.columns:
        if c in exclude:
            continue
        s = pd.to_numeric(df[c], errors="coerce")
        if s.notna().sum() > 0:
            feature_cols.append(c)

    if len(feature_cols) == 0:
        return None, None, None

    X = df[feature_cols].apply(pd.to_numeric, errors="coerce").values.astype(np.float32)
    for j in range(X.shape[1]):
        col = X[:, j]
        mask = np.isnan(col)
        if mask.all():
            X[:, j] = 0.0
        else:
            med = np.nanmedian(col)
            X[mask] = med

    if time_col is not None:
        tt = pd.to_datetime(df[time_col], errors="coerce")
    else:
        tt = pd.Series(range(len(df)))

    return X, tt, feature_cols

def compute_ra_features(record_dir, ts_times):
    meta = summarize_text_meta(record_dir)
    ts_span = np.nan
    if ts_times is not None and hasattr(ts_times, "notna") and ts_times.notna().sum() >= 2:
        ts_span = (ts_times.max() - ts_times.min()).total_seconds() / 3600.0

    txt_span = meta["txt_timespan_hours"]
    txt_gap = meta["txt_mean_gap_hours"]
    txt_missing = meta["txt_missing_frac"]
    n_txt = meta["txt_n_rows"]

    recency = 1.0 / (1.0 + max(txt_gap, 0.0)) if pd.notna(txt_gap) else 0.0
    alignment = (min(txt_span, ts_span) / max(ts_span, 1e-8)) if pd.notna(txt_span) and pd.notna(ts_span) and ts_span > 0 else 0.0
    density = (n_txt / max(txt_span, 1e-8)) if pd.notna(txt_span) and txt_span > 0 else 0.0
    missingness = 1.0 - txt_missing if pd.notna(txt_missing) else 0.0

    return np.array([recency, alignment, density, missingness], dtype=np.float32)

def robust_scale_fit(all_hist, all_fut):
    concat = np.concatenate([all_hist, all_fut], axis=0)  # [N, F]
    med = np.nanmedian(concat, axis=0)
    q75 = np.nanpercentile(concat, 75, axis=0)
    q25 = np.nanpercentile(concat, 25, axis=0)
    iqr = q75 - q25
    iqr[iqr == 0] = 1.0
    return med.astype(np.float32), iqr.astype(np.float32)

def robust_scale_apply(X, med, iqr):
    return (X - med) / iqr

def build_windows(processed_dir, config):
    record_dirs = sorted([p for p in Path(processed_dir).iterdir() if p.is_dir()])[:config["max_records_per_dataset"]]
    windows = []
    feature_dim_counter = {}

    for rd in record_dirs:
        try:
            X, tt, feat_cols = load_time_series(rd)
            if X is None:
                continue

            emb, n_notes = load_text_embedding(rd, config["llm_model"], config["llm_layers"])
            ra = compute_ra_features(rd, tt)

            T, F = X.shape
            total = config["history"] + config["pred_window"]
            if T < total:
                continue

            starts = list(range(0, T - total + 1, config["stride"]))[:config["max_windows_per_record"]]
            for s in starts:
                hist = X[s:s+config["history"]]
                fut = X[s+config["history"]:s+config["history"]+config["pred_window"]]
                if hist.shape[0] == config["history"] and fut.shape[0] == config["pred_window"]:
                    windows.append({
                        "record_id": rd.name,
                        "x_hist": hist.astype(np.float32),
                        "y_fut": fut.astype(np.float32),
                        "text_emb": emb.astype(np.float32) if emb is not None else None,
                        "ra": ra.astype(np.float32),
                        "n_features": F
                    })
                    feature_dim_counter[F] = feature_dim_counter.get(F, 0) + 1
        except Exception:
            continue

    if len(windows) == 0:
        raise RuntimeError("No valid windows found.")

    dominant_F = max(feature_dim_counter, key=feature_dim_counter.get)
    windows = [w for w in windows if w["n_features"] == dominant_F]

    emb_dim = None
    for w in windows:
        if w["text_emb"] is not None:
            emb_dim = len(w["text_emb"])
            break
    if emb_dim is None:
        emb_dim = 32

    for w in windows:
        if w["text_emb"] is None:
            w["text_emb"] = np.zeros(emb_dim, dtype=np.float32)

    # normalize X and Y jointly using robust scaling
    all_hist = np.concatenate([w["x_hist"] for w in windows], axis=0)
    all_fut = np.concatenate([w["y_fut"] for w in windows], axis=0)
    med, iqr = robust_scale_fit(all_hist, all_fut)

    for w in windows:
        w["x_hist_scaled"] = robust_scale_apply(w["x_hist"], med, iqr).astype(np.float32)
        w["y_fut_scaled"] = robust_scale_apply(w["y_fut"], med, iqr).astype(np.float32)

    return windows, dominant_F, emb_dim, med, iqr

class ForecastDataset(Dataset):
    def __init__(self, windows):
        self.windows = windows
    def __len__(self):
        return len(self.windows)
    def __getitem__(self, idx):
        w = self.windows[idx]
        return (
            torch.tensor(w["x_hist_scaled"], dtype=torch.float32),
            torch.tensor(w["y_fut_scaled"], dtype=torch.float32),
            torch.tensor(w["y_fut"], dtype=torch.float32),   # original scale for reporting
            torch.tensor(w["text_emb"], dtype=torch.float32),
            torch.tensor(w["ra"], dtype=torch.float32),
        )

class GRAddForecaster(nn.Module):
    def __init__(self, input_dim, emb_dim, hidden_dim, pred_window, gate_type="GR_Add"):
        super().__init__()
        self.input_dim = input_dim
        self.pred_window = pred_window
        self.gate_type = gate_type

        self.gru = nn.GRU(input_dim, hidden_dim, batch_first=True)
        self.text_proj = nn.Linear(emb_dim, hidden_dim)

        self.base_gate = nn.Sequential(
            nn.Linear(hidden_dim * 2, hidden_dim),
            nn.ReLU(),
            nn.Linear(hidden_dim, hidden_dim),
            nn.Sigmoid()
        )

        self.ra_proj = nn.Linear(4, hidden_dim)
        self.recency_head = nn.Linear(1, hidden_dim)
        self.align_head = nn.Linear(1, hidden_dim)
        self.density_head = nn.Linear(1, hidden_dim)
        self.missing_head = nn.Linear(1, hidden_dim)

        self.decoder = nn.Sequential(
            nn.Linear(hidden_dim, hidden_dim),
            nn.ReLU(),
            nn.Linear(hidden_dim, pred_window * input_dim)
        )

    def forward(self, x_hist, text_emb, ra):
        _, h = self.gru(x_hist)
        h = h[-1]
        t = self.text_proj(text_emb)

        if self.gate_type == "GR_Add":
            g = self.base_gate(torch.cat([h, t], dim=1))
        elif self.gate_type == "RA_full":
            g = torch.sigmoid(self.base_gate(torch.cat([h, t], dim=1)) + self.ra_proj(ra))
        elif self.gate_type == "RA_recency":
            g = torch.sigmoid(self.base_gate(torch.cat([h, t], dim=1)) + self.recency_head(ra[:, 0:1]))
        elif self.gate_type == "RA_alignment":
            g = torch.sigmoid(self.base_gate(torch.cat([h, t], dim=1)) + self.align_head(ra[:, 1:2]))
        elif self.gate_type == "RA_density":
            g = torch.sigmoid(self.base_gate(torch.cat([h, t], dim=1)) + self.density_head(ra[:, 2:3]))
        elif self.gate_type == "RA_missingness":
            g = torch.sigmoid(self.base_gate(torch.cat([h, t], dim=1)) + self.missing_head(ra[:, 3:4]))
        else:
            raise ValueError(self.gate_type)

        fused = h + g * t
        out = self.decoder(fused).view(-1, self.pred_window, self.input_dim)
        return out

def inverse_scale(X_scaled, med, iqr):
    return X_scaled * iqr.reshape(1, 1, -1) + med.reshape(1, 1, -1)

def run_experiment(windows, input_dim, emb_dim, med, iqr, gate_type):
    idx = np.arange(len(windows))
    train_idx, test_idx = train_test_split(idx, test_size=0.2, random_state=SEED)
    train_idx, val_idx = train_test_split(train_idx, test_size=0.2, random_state=SEED)

    train_ds = ForecastDataset([windows[i] for i in train_idx])
    val_ds   = ForecastDataset([windows[i] for i in val_idx])
    test_ds  = ForecastDataset([windows[i] for i in test_idx])

    train_loader = DataLoader(train_ds, batch_size=CONFIG["batch_size"], shuffle=True)
    val_loader   = DataLoader(val_ds, batch_size=CONFIG["batch_size"], shuffle=False)
    test_loader  = DataLoader(test_ds, batch_size=CONFIG["batch_size"], shuffle=False)

    model = GRAddForecaster(input_dim, emb_dim, CONFIG["hidden_dim"], CONFIG["pred_window"], gate_type).to(DEVICE)
    opt = torch.optim.Adam(model.parameters(), lr=CONFIG["lr"])
    loss_fn = nn.MSELoss()

    best_val = np.inf
    best_state = None
    hist_rows = []

    for epoch in range(1, CONFIG["epochs"] + 1):
        model.train()
        tr_losses = []
        for xh, yf_scaled, yf_orig, te, ra in train_loader:
            xh, yf_scaled, te, ra = xh.to(DEVICE), yf_scaled.to(DEVICE), te.to(DEVICE), ra.to(DEVICE)
            opt.zero_grad()
            pred_scaled = model(xh, te, ra)
            loss = loss_fn(pred_scaled, yf_scaled)
            loss.backward()
            nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            opt.step()
            tr_losses.append(loss.item())

        model.eval()
        va_losses = []
        with torch.no_grad():
            for xh, yf_scaled, yf_orig, te, ra in val_loader:
                xh, yf_scaled, te, ra = xh.to(DEVICE), yf_scaled.to(DEVICE), te.to(DEVICE), ra.to(DEVICE)
                pred_scaled = model(xh, te, ra)
                va_losses.append(loss_fn(pred_scaled, yf_scaled).item())

        tr = float(np.mean(tr_losses))
        va = float(np.mean(va_losses))
        hist_rows.append({"epoch": epoch, "train_mse_scaled": tr, "val_mse_scaled": va})

        if va < best_val:
            best_val = va
            best_state = {k: v.cpu().clone() for k, v in model.state_dict().items()}

    model.load_state_dict(best_state)
    pd.DataFrame(hist_rows).to_csv(OUT_ROOT / f"history_{gate_type}.csv", index=False)

    # evaluate on original scale
    model.eval()
    y_true_all, y_pred_all = [], []
    with torch.no_grad():
        for xh, yf_scaled, yf_orig, te, ra in test_loader:
            xh, te, ra = xh.to(DEVICE), te.to(DEVICE), ra.to(DEVICE)
            pred_scaled = model(xh, te, ra).cpu().numpy()
            pred_orig = inverse_scale(pred_scaled, med, iqr)
            y_pred_all.append(pred_orig)
            y_true_all.append(yf_orig.numpy())

    y_true = np.concatenate(y_true_all, axis=0)
    y_pred = np.concatenate(y_pred_all, axis=0)

    mse = mean_squared_error(y_true.reshape(-1), y_pred.reshape(-1))
    mae = mean_absolute_error(y_true.reshape(-1), y_pred.reshape(-1))
    rmse = float(np.sqrt(mse))

    plt.figure(figsize=(7,4))
    plt.plot(y_true[0, :, 0], label="True")
    plt.plot(y_pred[0, :, 0], label="Pred")
    plt.title(f"FNSPID normalized: {gate_type}")
    plt.xlabel("Forecast step")
    plt.ylabel("Value")
    plt.legend()
    plt.tight_layout()
    plt.savefig(OUT_ROOT / f"sample_forecast_{gate_type}.png", dpi=220)
    plt.close()

    return {
        "gate_type": gate_type,
        "n_windows_total": len(windows),
        "n_train": len(train_ds),
        "n_val": len(val_ds),
        "n_test": len(test_ds),
        "input_dim": input_dim,
        "emb_dim": emb_dim,
        "best_val_mse_scaled": best_val,
        "test_mse": mse,
        "test_rmse": rmse,
        "test_mae": mae
    }

