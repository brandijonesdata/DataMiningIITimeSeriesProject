def run_one_experiment(windows, input_dim, emb_dim, gate_type, config, out_dir):
    idx = np.arange(len(windows))
    train_idx, test_idx = train_test_split(idx, test_size=0.2, random_state=SEED)
    train_idx, val_idx = train_test_split(train_idx, test_size=0.2, random_state=SEED)

    train_ds = ForecastDataset([windows[i] for i in train_idx])
    val_ds   = ForecastDataset([windows[i] for i in val_idx])
    test_ds  = ForecastDataset([windows[i] for i in test_idx])

    train_loader = DataLoader(train_ds, batch_size=config["batch_size"], shuffle=True)
    val_loader   = DataLoader(val_ds,   batch_size=config["batch_size"], shuffle=False)
    test_loader  = DataLoader(test_ds,  batch_size=config["batch_size"], shuffle=False)

    model = GRAddForecaster(
        input_dim=input_dim,
        emb_dim=emb_dim,
        hidden_dim=config["hidden_dim"],
        pred_window=config["pred_window"],
        gate_type=gate_type
    ).to(DEVICE)

    optimizer = torch.optim.Adam(model.parameters(), lr=config["lr"])
    criterion = nn.MSELoss()

    best_val = np.inf
    best_state = None
    history_rows = []

    for epoch in range(1, config["epochs"] + 1):
        model.train()
        train_losses = []
        for xh, yf, te, ra in train_loader:
            xh, yf, te, ra = xh.to(DEVICE), yf.to(DEVICE), te.to(DEVICE), ra.to(DEVICE)
            optimizer.zero_grad()
            pred = model(xh, te, ra)
            loss = criterion(pred, yf)
            loss.backward()
            optimizer.step()
            train_losses.append(loss.item())

        model.eval()
        val_losses = []
        with torch.no_grad():
            for xh, yf, te, ra in val_loader:
                xh, yf, te, ra = xh.to(DEVICE), yf.to(DEVICE), te.to(DEVICE), ra.to(DEVICE)
                pred = model(xh, te, ra)
                loss = criterion(pred, yf)
                val_losses.append(loss.item())

        tr = float(np.mean(train_losses)) if train_losses else np.nan
        va = float(np.mean(val_losses)) if val_losses else np.nan
        history_rows.append({"epoch": epoch, "train_mse": tr, "val_mse": va})

        if va < best_val:
            best_val = va
            best_state = {k: v.cpu().clone() for k, v in model.state_dict().items()}

    if best_state is not None:
        model.load_state_dict(best_state)

    # test metrics
    model.eval()
    y_true_all, y_pred_all = [], []
    with torch.no_grad():
        for xh, yf, te, ra in test_loader:
            xh, yf, te, ra = xh.to(DEVICE), yf.to(DEVICE), te.to(DEVICE), ra.to(DEVICE)
            pred = model(xh, te, ra)
            y_true_all.append(yf.cpu().numpy())
            y_pred_all.append(pred.cpu().numpy())

    y_true = np.concatenate(y_true_all, axis=0)
    y_pred = np.concatenate(y_pred_all, axis=0)

    mse = mean_squared_error(y_true.reshape(-1), y_pred.reshape(-1))
    mae = mean_absolute_error(y_true.reshape(-1), y_pred.reshape(-1))
    rmse = float(np.sqrt(mse))

    # save training history
    hist_df = pd.DataFrame(history_rows)
    hist_df.to_csv(out_dir / f"history_{gate_type}.csv", index=False)

    # save sample plot
    samp_true = y_true[0, :, 0]
    samp_pred = y_pred[0, :, 0]
    plt.figure(figsize=(7,4))
    plt.plot(range(len(samp_true)), samp_true, label="True")
    plt.plot(range(len(samp_pred)), samp_pred, label="Pred")
    plt.title(f"{gate_type}: sample forecast")
    plt.xlabel("Forecast step")
    plt.ylabel("Value")
    plt.legend()
    plt.tight_layout()
    plt.savefig(out_dir / f"sample_forecast_{gate_type}.png", dpi=220)
    plt.close()

    return {
        "gate_type": gate_type,
        "n_windows_total": len(windows),
        "n_train": len(train_ds),
        "n_val": len(val_ds),
        "n_test": len(test_ds),
        "input_dim": input_dim,
        "emb_dim": emb_dim,
        "best_val_mse": best_val,
        "test_mse": mse,
        "test_rmse": rmse,
        "test_mae": mae,
    }

