def load_text_embedding(record_dir, llm_model="GPT2", llm_layers=6):
    emb_file = find_embedding_file(record_dir, llm_model, llm_layers)
    if emb_file is None:
        return None, 0

    try:
        obj = torch.load(emb_file, map_location="cpu")
        arr = None
        if isinstance(obj, dict):
            for key in ["embeddings", "embedding", "data", "text_embeddings", "x"]:
                if key in obj:
                    arr = obj[key]
                    break
            if arr is None:
                for v in obj.values():
                    if hasattr(v, "shape"):
                        arr = v
                        break
        else:
            arr = obj

        if arr is None:
            return None, 0

        if hasattr(arr, "detach"):
            arr = arr.detach().cpu().numpy()
        else:
            arr = np.asarray(arr)

        if arr.ndim == 1:
            arr = arr.reshape(1, -1)

        if arr.size == 0:
            return None, 0

        mean_emb = arr.mean(axis=0).astype(np.float32)
        return mean_emb, arr.shape[0]
    except Exception:
        return None, 0

def summarize_text_meta(record_dir):
    text_path = record_dir / "text.csv"
    if not text_path.exists():
        return dict(txt_n_rows=0.0, txt_missing_frac=np.nan, txt_timespan_hours=np.nan, txt_mean_gap_hours=np.nan)

    try:
        df = pd.read_csv(text_path)
    except Exception:
        return dict(txt_n_rows=0.0, txt_missing_frac=np.nan, txt_timespan_hours=np.nan, txt_mean_gap_hours=np.nan)

    time_col = find_time_col(df)
    if time_col is not None:
        tt = pd.to_datetime(df[time_col], errors="coerce")
    else:
        tt = pd.Series([pd.NaT] * len(df))

    exclude = set(TIME_CANDIDATES + ["record_id"])
    txt_cols = [c for c in df.columns if c not in exclude]
    txt_missing = float(df[txt_cols[0]].isna().mean()) if len(txt_cols) >= 1 else np.nan

    out = {
        "txt_n_rows": float(len(df)),
        "txt_missing_frac": txt_missing,
        "txt_timespan_hours": np.nan,
        "txt_mean_gap_hours": np.nan,
    }

    if tt.notna().sum() >= 2:
        tt2 = tt.sort_values()
        diffs = tt2.diff().dropna().dt.total_seconds() / 3600.0
        out["txt_timespan_hours"] = float((tt.max() - tt.min()).total_seconds() / 3600.0)
        out["txt_mean_gap_hours"] = float(diffs.mean()) if len(diffs) else np.nan

    return out

def load_time_series(record_dir):
    ts_path = record_dir / "time_series.csv"
    if not ts_path.exists():
        return None, None, None

    df = pd.read_csv(ts_path)
    if df.empty:
        return None, None, None

    time_col = find_time_col(df)

    # choose numeric feature columns
    exclude = set([time_col] if time_col is not None else [])
    exclude.update(["record_id"])
    feature_cols = []
    for c in df.columns:
        if c in exclude:
            continue
        s = pd.to_numeric(df[c], errors="coerce")
        if s.notna().sum() > 0:
            feature_cols.append(c)

    if len(feature_cols) == 0:
        return None, None, None

    X = df[feature_cols].apply(pd.to_numeric, errors="coerce").values.astype(np.float32)

    # simple median imputation per column
    for j in range(X.shape[1]):
        col = X[:, j]
        mask = np.isnan(col)
        if mask.all():
            X[:, j] = 0.0
        else:
            med = np.nanmedian(col)
            X[mask, j] = med

    if time_col is not None:
        tt = pd.to_datetime(df[time_col], errors="coerce")
    else:
        tt = pd.Series(range(len(df)))
    return X, tt, feature_cols

def compute_ra_features(record_dir, ts_times):
    meta = summarize_text_meta(record_dir)
    ts_span = np.nan
    ts_mean_gap = np.nan
    if ts_times is not None and hasattr(ts_times, "notna") and ts_times.notna().sum() >= 2:
        ts_span = (ts_times.max() - ts_times.min()).total_seconds() / 3600.0
        diffs = ts_times.sort_values().diff().dropna().dt.total_seconds() / 3600.0
        ts_mean_gap = float(diffs.mean()) if len(diffs) else np.nan

    txt_span = meta["txt_timespan_hours"]
    txt_gap = meta["txt_mean_gap_hours"]
    txt_missing = meta["txt_missing_frac"]
    n_txt = meta["txt_n_rows"]

    recency = 1.0 / (1.0 + max(txt_gap, 0.0)) if pd.notna(txt_gap) else 0.0
    alignment = (min(txt_span, ts_span) / max(ts_span, 1e-8)) if pd.notna(txt_span) and pd.notna(ts_span) and ts_span > 0 else 0.0

    if pd.notna(txt_span) and pd.notna(ts_span) and txt_span > 0 and ts_span > 0:
        txt_density = n_txt / txt_span
        ts_density = len(ts_times) / ts_span if ts_times is not None else np.nan
        density = txt_density / max(ts_density, 1e-8) if pd.notna(ts_density) else 0.0
    else:
        density = 0.0

    missingness = 1.0 - txt_missing if pd.notna(txt_missing) else 0.0
    return np.array([recency, alignment, density, missingness], dtype=np.float32)

def build_windows_for_record(record_dir, history=30, pred_window=30, stride=30, llm_model="GPT2", llm_layers=6, max_windows=8):
    X, tt, feat_cols = load_time_series(record_dir)
    if X is None:
        return []

    emb, n_notes = load_text_embedding(record_dir, llm_model=llm_model, llm_layers=llm_layers)
    ra = compute_ra_features(record_dir, tt)

    T, F = X.shape
    total = history + pred_window
    if T < total:
        return []

    windows = []
    starts = list(range(0, T - total + 1, stride))[:max_windows]
    for s in starts:
        hist = X[s:s+history]
        fut = X[s+history:s+history+pred_window]
        if hist.shape[0] != history or fut.shape[0] != pred_window:
            continue
        windows.append({
            "record_id": record_dir.name,
            "x_hist": hist.astype(np.float32),
            "y_fut": fut.astype(np.float32),
            "text_emb": emb.astype(np.float32) if emb is not None else None,
            "ra": ra.astype(np.float32),
            "n_notes": float(n_notes),
            "n_features": F,
        })
    return windows

def load_dataset_windows(processed_dir, config):
    processed_dir = Path(processed_dir)
    record_dirs = sorted([p for p in processed_dir.iterdir() if p.is_dir()])[:config["max_records_per_dataset"]]
    all_windows = []
    feature_dim_counter = {}

    for i, rd in enumerate(record_dirs):
        try:
            ws = build_windows_for_record(
                rd,
                history=config["history"],
                pred_window=config["pred_window"],
                stride=config["stride"],
                llm_model=config["llm_model"],
                llm_layers=config["llm_layers"],
                max_windows=config["max_windows_per_record"],
            )
            if len(ws):
                F = ws[0]["n_features"]
                feature_dim_counter[F] = feature_dim_counter.get(F, 0) + len(ws)
                all_windows.extend(ws)
        except Exception:
            continue

    if len(all_windows) == 0:
        raise RuntimeError(f"No valid forecasting windows found in {processed_dir}")

    # keep only dominant feature dimension
    dominant_F = max(feature_dim_counter, key=feature_dim_counter.get)
    all_windows = [w for w in all_windows if w["n_features"] == dominant_F]

    # fill missing embeddings with zeros after we know dim
    emb_dim = None
    for w in all_windows:
        if w["text_emb"] is not None:
            emb_dim = len(w["text_emb"])
            break
    if emb_dim is None:
        emb_dim = 32  # fallback small dim if no embeddings available

    for w in all_windows:
        if w["text_emb"] is None:
            w["text_emb"] = np.zeros(emb_dim, dtype=np.float32)

    return all_windows, dominant_F, emb_dim

class ForecastDataset(Dataset):
    def __init__(self, windows):
        self.windows = windows
    def __len__(self):
        return len(self.windows)
    def __getitem__(self, idx):
        w = self.windows[idx]
        return (
            torch.tensor(w["x_hist"], dtype=torch.float32),
            torch.tensor(w["y_fut"], dtype=torch.float32),
            torch.tensor(w["text_emb"], dtype=torch.float32),
            torch.tensor(w["ra"], dtype=torch.float32),
        )

