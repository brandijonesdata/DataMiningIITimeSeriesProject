# Original GR-Add and RA-GR-Add fusion
# ======================================
class OriginalGRAddFusion(nn.Module):
    """
    Simple additive gating baseline mimicking the paper's GR-Add spirit:
      g = sigmoid(W[h_ts ; h_txt])
      z = g * h_ts + (1-g) * h_txt
    """

    def __init__(self, ts_dim: int, txt_dim: int, hidden_dim: int) -> None:
        super().__init__()
        self.ts_proj = nn.Linear(ts_dim, hidden_dim)
        self.txt_proj = nn.Linear(txt_dim, hidden_dim)
        self.gate = nn.Linear(hidden_dim * 2, hidden_dim)

    def forward(self, ts_global: torch.Tensor, text_seq: torch.Tensor) -> Tuple[torch.Tensor, Dict[str, torch.Tensor]]:
        txt_global = text_seq.mean(dim=1)
        h_ts = self.ts_proj(ts_global)
        h_txt = self.txt_proj(txt_global)
        alpha = torch.sigmoid(self.gate(torch.cat([h_ts, h_txt], dim=-1)))
        fused = alpha * h_ts + (1.0 - alpha) * h_txt
        extras = {"alpha": alpha}
        return fused, extras


class ReliabilityAwareGRAddFusion(nn.Module):
    """
    Reliability-aware GR-Add with four reliability factors:
      - recency
      - alignment
      - density
      - missingness

    Full gate:
      r = w_r * r_rec + w_a * r_align + w_d * r_dense + w_m * r_miss
      alpha = sigmoid(W[h_ts ; h_txt ; r])
      z = alpha * h_ts + (1-alpha) * h_txt

    Variants supported by mode:
      - full
      - recency
      - alignment
      - density
      - missingness
    """

    def __init__(self, ts_dim: int, txt_dim: int, hidden_dim: int, mode: str = "full") -> None:
        super().__init__()
        self.mode = mode
        self.ts_proj = nn.Linear(ts_dim, hidden_dim)
        self.txt_proj = nn.Linear(txt_dim, hidden_dim)
        self.rel_proj = nn.Linear(4, hidden_dim)
        self.gate = nn.Linear(hidden_dim * 3, hidden_dim)
        self.mix_weights = nn.Parameter(torch.tensor([1.0, 1.0, 1.0, 1.0], dtype=torch.float32))

    def _compute_reliability(
        self,
        ts_global: torch.Tensor,
        h_seq: torch.Tensor,
        mask: torch.Tensor,
        t: torch.Tensor,
        text_seq: torch.Tensor,
        text_t: torch.Tensor,
    ) -> torch.Tensor:
        B, T, H = h_seq.shape
        N = text_seq.shape[1]

        # recency: recent notes get higher reliability
        ts_end = t.max(dim=1).values.unsqueeze(1)           # [B,1]
        recency_dist = torch.clamp(ts_end - text_t, min=0.0)
        recency_score = torch.exp(-recency_dist / (recency_dist.std() + 1e-6)).mean(dim=1)  # [B]

        # alignment: cosine similarity between TS global and text global
        txt_global = text_seq.mean(dim=1)
        alignment_score = F.cosine_similarity(ts_global, txt_global, dim=-1)  # [B]
        alignment_score = (alignment_score + 1.0) / 2.0

        # density: denser observed signals imply more reliable structured stream
        density_score = mask.mean(dim=(1, 2))  # [B]

        # missingness reliability: inverse missingness
        missingness_score = 1.0 - (1.0 - mask).mean(dim=(1, 2))

        rel = torch.stack(
            [recency_score, alignment_score, density_score, missingness_score], dim=-1
        )  # [B,4]

        if self.mode == "recency":
            rel[:, 1:] = 0.0
        elif self.mode == "alignment":
            rel[:, [0, 2, 3]] = 0.0
        elif self.mode == "density":
            rel[:, [0, 1, 3]] = 0.0
        elif self.mode == "missingness":
            rel[:, :3] = 0.0
        return rel

    def forward(
        self,
        ts_global: torch.Tensor,
        h_seq: torch.Tensor,
        mask: torch.Tensor,
        t: torch.Tensor,
        text_seq: torch.Tensor,
        text_t: torch.Tensor,
    ) -> Tuple[torch.Tensor, Dict[str, torch.Tensor]]:
        h_ts = self.ts_proj(ts_global)
        txt_global = text_seq.mean(dim=1)
        h_txt = self.txt_proj(txt_global)
        rel = self._compute_reliability(ts_global, h_seq, mask, t, text_seq, text_t)
        rel_weighted = rel * torch.softmax(self.mix_weights, dim=0)
        h_rel = self.rel_proj(rel_weighted)
        alpha = torch.sigmoid(self.gate(torch.cat([h_ts, h_txt, h_rel], dim=-1)))
        fused = alpha * h_ts + (1.0 - alpha) * h_txt
        extras = {"alpha": alpha, "reliability": rel, "reliability_weighted": rel_weighted}
        return fused, extras


class ReadmissionIMMClassifier(nn.Module):
    def __init__(
        self,
        in_dim: int,
        text_dim: int,
        hidden_dim: int = 64,
        fusion_type: str = "gradd",
    ) -> None:
        super().__init__()
        self.encoder = SimpleTimeEncoder(in_dim=in_dim, hidden_dim=hidden_dim)
        self.text_proj = nn.Linear(text_dim, hidden_dim)

        if fusion_type == "gradd":
            self.fusion = OriginalGRAddFusion(hidden_dim, hidden_dim, hidden_dim)
        elif fusion_type in {"ra_full", "ra_recency", "ra_alignment", "ra_density", "ra_missingness"}:
            mode_map = {
                "ra_full": "full",
                "ra_recency": "recency",
                "ra_alignment": "alignment",
                "ra_density": "density",
                "ra_missingness": "missingness",
            }
            self.fusion = ReliabilityAwareGRAddFusion(
                ts_dim=hidden_dim,
                txt_dim=hidden_dim,
                hidden_dim=hidden_dim,
                mode=mode_map[fusion_type],
            )
        else:
            raise ValueError(f"Unknown fusion_type: {fusion_type}")

        self.classifier = nn.Sequential(
            nn.LayerNorm(hidden_dim),
            nn.Linear(hidden_dim, hidden_dim),
            nn.GELU(),
            nn.Dropout(0.1),
            nn.Linear(hidden_dim, 1),
        )

    def forward(self, batch: BatchTensors) -> Tuple[torch.Tensor, Dict[str, torch.Tensor]]:
        x = batch.x.float()
        m = batch.m.float()
        t = batch.t.float()
        te = batch.text_emb.float()
        tt = batch.text_t.float()

        h_seq, ts_global = self.encoder(x, m, t)
        te = self.text_proj(te)

        if isinstance(self.fusion, OriginalGRAddFusion):
            fused, extras = self.fusion(ts_global, te)
        else:
            fused, extras = self.fusion(ts_global, h_seq, m, t, te, tt)

        logits = self.classifier(fused).squeeze(-1)
        return logits, extras


