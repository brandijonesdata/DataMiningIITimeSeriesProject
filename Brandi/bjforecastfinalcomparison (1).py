class GRAddForecaster(nn.Module):
    def __init__(self, input_dim, emb_dim, hidden_dim, pred_window, gate_type="GR_Add"):
        super().__init__()
        self.input_dim = input_dim
        self.emb_dim = emb_dim
        self.hidden_dim = hidden_dim
        self.pred_window = pred_window
        self.gate_type = gate_type

        self.gru = nn.GRU(input_dim, hidden_dim, batch_first=True)
        self.text_proj = nn.Linear(emb_dim, hidden_dim)

        # original gate
        self.base_gate = nn.Sequential(
            nn.Linear(hidden_dim * 2, hidden_dim),
            nn.ReLU(),
            nn.Linear(hidden_dim, hidden_dim),
            nn.Sigmoid()
        )

        # RA gates
        self.ra_proj = nn.Linear(4, hidden_dim)
        self.recency_head = nn.Linear(1, hidden_dim)
        self.align_head = nn.Linear(1, hidden_dim)
        self.density_head = nn.Linear(1, hidden_dim)
        self.missing_head = nn.Linear(1, hidden_dim)

        self.decoder = nn.Sequential(
            nn.Linear(hidden_dim, hidden_dim),
            nn.ReLU(),
            nn.Linear(hidden_dim, pred_window * input_dim)
        )

    def forward(self, x_hist, text_emb, ra):
        # x_hist: [B,H,F], text_emb: [B,E], ra: [B,4]
        _, h = self.gru(x_hist)           # [1,B,Hid]
        h = h[-1]                         # [B,Hid]
        t = self.text_proj(text_emb)      # [B,Hid]

        if self.gate_type == "GR_Add":
            g = self.base_gate(torch.cat([h, t], dim=1))
        elif self.gate_type == "RA_full":
            g = torch.sigmoid(self.base_gate(torch.cat([h, t], dim=1)) + self.ra_proj(ra))
        elif self.gate_type == "RA_recency":
            g = torch.sigmoid(self.base_gate(torch.cat([h, t], dim=1)) + self.recency_head(ra[:, 0:1]))
        elif self.gate_type == "RA_alignment":
            g = torch.sigmoid(self.base_gate(torch.cat([h, t], dim=1)) + self.align_head(ra[:, 1:2]))
        elif self.gate_type == "RA_density":
            g = torch.sigmoid(self.base_gate(torch.cat([h, t], dim=1)) + self.density_head(ra[:, 2:3]))
        elif self.gate_type == "RA_missingness":
            g = torch.sigmoid(self.base_gate(torch.cat([h, t], dim=1)) + self.missing_head(ra[:, 3:4]))
        else:
            raise ValueError(self.gate_type)

        fused = h + g * t
        out = self.decoder(fused).view(-1, self.pred_window, self.input_dim)
        return out

