# Simplified temporal backbone
# ==============================
class SimpleTimeEncoder(nn.Module):
    """
    A lightweight substitute for the forecasting backbone when we do the classification extension.
    This is not claimed to be identical to tPatchGNN; it is a controlled extension module.

    Input:
      x: [B, T, C]
      m: [B, T, C]
      t: [B, T]
    Output:
      h: [B, T, H]
      g: [B, H] pooled time-series representation
    """

    def __init__(self, in_dim: int, hidden_dim: int = 64) -> None:
        super().__init__()
        self.input_proj = nn.Linear(in_dim * 2 + 1, hidden_dim)
        self.gru = nn.GRU(hidden_dim, hidden_dim, batch_first=True)
        self.norm = nn.LayerNorm(hidden_dim)

    def forward(self, x: torch.Tensor, m: torch.Tensor, t: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor]:
        dt = t.clone()
        dt[:, 1:] = t[:, 1:] - t[:, :-1]
        dt[:, 0] = 0.0
        feats = torch.cat([x, m, dt.unsqueeze(-1)], dim=-1)
        z = self.input_proj(feats)
        h, _ = self.gru(z)
        h = self.norm(h)
        g = h.mean(dim=1)
        return h, g


