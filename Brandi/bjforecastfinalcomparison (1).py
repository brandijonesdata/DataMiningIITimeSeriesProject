def parse_metrics_from_log(text: str) -> Dict[str, Optional[float]]:
    """
    Heuristic parser for common forecasting log outputs.
    Adjust regexes to your repo's exact print format if needed.
    """
    patterns = {
        "mse": r"(?:MSE|mse)\s*[:=]\s*([0-9eE+\-.]+)",
        "mae": r"(?:MAE|mae)\s*[:=]\s*([0-9eE+\-.]+)",
        "rmse": r"(?:RMSE|rmse)\s*[:=]\s*([0-9eE+\-.]+)",
        "mape": r"(?:MAPE|mape)\s*[:=]\s*([0-9eE+\-.]+)",
        "auc": r"(?:AUC|auc)\s*[:=]\s*([0-9eE+\-.]+)",
        "auprc": r"(?:AUPRC|auprc)\s*[:=]\s*([0-9eE+\-.]+)",
    }
    out: Dict[str, Optional[float]] = {}
    for key, pat in patterns.items():
        matches = re.findall(pat, text)
        out[key] = float(matches[-1]) if matches else None
    return out


