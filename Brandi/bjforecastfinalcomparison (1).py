ra_gate_code = r'''
import torch
import torch.nn as nn
import torch.nn.functional as F

class MMF_RA_GRAdd(nn.Module):
    """
    Recency/Alignment/Density/Missingness-aware GR residual-add fusion.

    Inputs:
      Y_ts  : [B, T, C]   base time-series forecast
      E_txt : [B, T, d]   text representation aligned to forecast steps
      M_txt : [B] or [B,T] text-availability mask

    Gate logic:
      delta = residual correction from GRU over [Y_ts, E_txt]
      g = sigmoid(base + wr*r + wa*a + wd*d + wm*m)
      Y_out = g*Y_ts + (1-g)*(Y_ts + delta)

    Component toggles allow:
      full, recency-only, alignment-only, density-only, missingness-only
    """

    def __init__(
        self,
        d_txt: int,
        C: int,
        hidden_dim: int,
        dropout: float = 0.1,
        use_recency: bool = True,
        use_alignment: bool = True,
        use_density: bool = True,
        use_missingness: bool = True,
    ):
        super().__init__()
        self.C = C
        self.d_txt = d_txt
        self.use_recency = use_recency
        self.use_alignment = use_alignment
        self.use_density = use_density
        self.use_missingness = use_missingness

        self.gru = nn.GRU(input_size=C + d_txt, hidden_size=hidden_dim, batch_first=True)
        self.residual_head = nn.Linear(hidden_dim, C)

        self.base_gate = nn.Linear(C + d_txt, C)

        # projections for alignment
        self.ts_proj = nn.Linear(C, C, bias=False)
        self.txt_proj = nn.Linear(d_txt, C, bias=False)

        # learnable scalar weights
        self.w_recency = nn.Parameter(torch.tensor(1.0))
        self.w_align = nn.Parameter(torch.tensor(1.0))
        self.w_density = nn.Parameter(torch.tensor(1.0))
        self.w_missing = nn.Parameter(torch.tensor(1.0))

        self.layer_norm = nn.LayerNorm(C)
        self.dropout = nn.Dropout(dropout)

    def _expand_mask(self, M_txt, B, T, C, device):
        if M_txt.dim() == 1:
            m = M_txt.view(B, 1, 1).float().expand(-1, T, C)
        elif M_txt.dim() == 2:
            m = M_txt.unsqueeze(-1).float().expand(-1, -1, C)
        else:
            m = M_txt.float()
            if m.shape[-1] != C:
                m = m.expand(-1, -1, C)
        return m.to(device)

    def forward(self, Y_ts, E_txt, M_txt):
        B, T, C = Y_ts.shape
        x = torch.cat([Y_ts, E_txt], dim=-1)              # [B,T,C+d]
        h, _ = self.gru(x)
        delta = self.residual_head(h)                     # [B,T,C]
        delta = self.dropout(self.layer_norm(delta))

        base_logits = self.base_gate(x)                   # [B,T,C]

        # 1) Recency proxy:
        # Since TTF_RecAvg already recency-weights text, larger ||E_txt|| is treated
        # as stronger recent textual support at that forecast step.
        recency_score = torch.norm(E_txt, dim=-1, keepdim=True)   # [B,T,1]
        recency_score = recency_score / (recency_score.mean(dim=1, keepdim=True) + 1e-8)
        recency_score = recency_score.expand(-1, -1, C)

        # 2) Alignment:
        # cosine similarity between projected TS and text representations
        ts_p = self.ts_proj(Y_ts)
        txt_p = self.txt_proj(E_txt)
        align_score = F.cosine_similarity(ts_p, txt_p, dim=-1).unsqueeze(-1)  # [B,T,1]
        align_score = align_score.expand(-1, -1, C)

        # 3) Density proxy:
        # local signal activity / complexity from absolute first difference in forecast
        dy = torch.zeros_like(Y_ts)
        dy[:, 1:, :] = torch.abs(Y_ts[:, 1:, :] - Y_ts[:, :-1, :])
        density_score = dy / (dy.mean(dim=1, keepdim=True) + 1e-8)

        # 4) Missingness:
        miss_score = self._expand_mask(M_txt, B, T, C, Y_ts.device)

        logits = base_logits
        if self.use_recency:
            logits = logits + self.w_recency * recency_score
        if self.use_alignment:
            logits = logits + self.w_align * align_score
        if self.use_density:
            logits = logits + self.w_density * density_score
        if self.use_missingness:
            logits = logits + self.w_missing * miss_score

        g = torch.sigmoid(logits)
        Y_out = g * Y_ts + (1.0 - g) * (Y_ts + delta)
        return Y_out
'''
