def binary_classification_metrics(y_true: np.ndarray, y_prob: np.ndarray) -> Dict[str, float]:
    y_pred = (y_prob >= 0.5).astype(int)
    acc = float((y_pred == y_true).mean())
    tp = float(((y_pred == 1) & (y_true == 1)).sum())
    tn = float(((y_pred == 0) & (y_true == 0)).sum())
    fp = float(((y_pred == 1) & (y_true == 0)).sum())
    fn = float(((y_pred == 0) & (y_true == 1)).sum())
    sens = tp / (tp + fn + 1e-8)
    spec = tn / (tn + fp + 1e-8)
    prec = tp / (tp + fp + 1e-8)
    f1 = 2 * prec * sens / (prec + sens + 1e-8)

    # ROC-AUC via rank statistic, sklearn-free
    pos = y_prob[y_true == 1]
    neg = y_prob[y_true == 0]
    if len(pos) == 0 or len(neg) == 0:
        auc = float("nan")
    else:
        wins = 0.0
        ties = 0.0
        for p in pos:
            wins += np.sum(p > neg)
            ties += np.sum(p == neg)
        auc = float((wins + 0.5 * ties) / (len(pos) * len(neg)))

    return {
        "accuracy": acc,
        "sensitivity": float(sens),
        "specificity": float(spec),
        "precision": float(prec),
        "f1": float(f1),
        "auc": float(auc),
        "n": int(len(y_true)),
        "n_pos": int((y_true == 1).sum()),
        "n_neg": int((y_true == 0).sum()),
    }


def split_indices(n: int, seed: int = 42, train_frac: float = 0.7, val_frac: float = 0.15) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    rng = np.random.default_rng(seed)
    idx = np.arange(n)
    rng.shuffle(idx)
    n_train = int(round(n * train_frac))
    n_val = int(round(n * val_frac))
    train_idx = idx[:n_train]
    val_idx = idx[n_train:n_train + n_val]
    test_idx = idx[n_train + n_val:]
    return train_idx, val_idx, test_idx


def subset_dataset(ds: ICUReadmissionWindowDataset, idx: np.ndarray) -> List[RecordExample]:
    return [ds.examples[int(i)] for i in idx]


def make_loader(examples: List[RecordExample], batch_size: int, shuffle: bool) -> DataLoader:
    return DataLoader(examples, batch_size=batch_size, shuffle=shuffle, collate_fn=collate_readmission)


def run_epoch(
    model: nn.Module,
    loader: DataLoader,
    optimizer: Optional[torch.optim.Optimizer],
    device: torch.device,
) -> Tuple[float, Dict[str, float]]:
    is_train = optimizer is not None
    model.train(is_train)
    losses: List[float] = []
    y_all: List[np.ndarray] = []
    p_all: List[np.ndarray] = []
    criterion = nn.BCEWithLogitsLoss()

    for batch in loader:
        batch = BatchTensors(
            x=batch.x.to(device),
            m=batch.m.to(device),
            t=batch.t.to(device),
            text_emb=batch.text_emb.to(device),
            text_t=batch.text_t.to(device),
            y=batch.y.to(device),
            ids=batch.ids,
        )
        logits, _ = model(batch)
        loss = criterion(logits, batch.y.float())

        if is_train:
            optimizer.zero_grad(set_to_none=True)
            loss.backward()
            optimizer.step()

        losses.append(float(loss.item()))
        y_all.append(batch.y.detach().cpu().numpy())
        p_all.append(torch.sigmoid(logits).detach().cpu().numpy())

    y = np.concatenate(y_all)
    p = np.concatenate(p_all)
    metrics = binary_classification_metrics(y.astype(int), p)
    metrics["loss"] = float(np.mean(losses)) if losses else float("nan")
    return metrics["loss"], metrics


def train_one_fusion_setting(
    processed_dir: Path,
    out_dir: Path,
    fusion_type: str,
    llm_model: str,
    llm_layers: int,
    max_records: Optional[int],
    seed: int,
    epochs: int,
    batch_size: int,
    lr: float,
    hidden_dim: int,
    device: torch.device,
) -> Dict[str, object]:
    set_seed(seed)
    ds = ICUReadmissionWindowDataset(
        processed_dir=processed_dir,
        llm_model=llm_model,
        llm_layers=llm_layers,
        max_records=max_records,
    )
    tr, va, te = split_indices(len(ds), seed=seed)
    train_loader = make_loader(subset_dataset(ds, tr), batch_size=batch_size, shuffle=True)
    val_loader = make_loader(subset_dataset(ds, va), batch_size=batch_size, shuffle=False)
    test_loader = make_loader(subset_dataset(ds, te), batch_size=batch_size, shuffle=False)

    in_dim = ds.examples[0].values.shape[1]
    text_dim = ds.examples[0].text_emb.shape[1]

    model = ReadmissionIMMClassifier(
        in_dim=in_dim,
        text_dim=text_dim,
        hidden_dim=hidden_dim,
        fusion_type=fusion_type,
    ).to(device)
    optimizer = torch.optim.Adam(model.parameters(), lr=lr)

    best_val_auc = -np.inf
    best_state: Optional[Dict[str, torch.Tensor]] = None
    history_rows: List[Dict[str, object]] = []

    for epoch in range(1, epochs + 1):
        _, train_metrics = run_epoch(model, train_loader, optimizer, device)
        _, val_metrics = run_epoch(model, val_loader, None, device)
        row = {
            "epoch": epoch,
            "fusion_type": fusion_type,
            **{f"train_{k}": v for k, v in train_metrics.items()},
            **{f"val_{k}": v for k, v in val_metrics.items()},
        }
        history_rows.append(row)
        if np.isfinite(val_metrics["auc"]) and val_metrics["auc"] > best_val_auc:
            best_val_auc = val_metrics["auc"]
            best_state = {k: v.detach().cpu().clone() for k, v in model.state_dict().items()}
        print(
            f"[{fusion_type}] epoch {epoch:03d} | "
            f"train_auc={train_metrics['auc']:.4f} val_auc={val_metrics['auc']:.4f} "
            f"val_f1={val_metrics['f1']:.4f}"
        )

    if best_state is not None:
        model.load_state_dict(best_state)
    _, test_metrics = run_epoch(model, test_loader, None, device)

    out_dir.mkdir(parents=True, exist_ok=True)
    pd.DataFrame(history_rows).to_csv(out_dir / f"history_{fusion_type}.csv", index=False)
    torch.save(model.state_dict(), out_dir / f"model_{fusion_type}.pt")

    result = {
        "fusion_type": fusion_type,
        "best_val_auc": float(best_val_auc),
        **{f"test_{k}": v for k, v in test_metrics.items()},
        "n_total": len(ds),
        "n_train": len(tr),
        "n_val": len(va),
        "n_test": len(te),
    }
    return result


