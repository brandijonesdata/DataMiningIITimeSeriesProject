def load_text_embedding_pt(path: Path) -> Tuple[np.ndarray, np.ndarray]:
    obj = torch.load(path, map_location="cpu")
    # support common save structures
    if isinstance(obj, dict):
        emb = None
        rel = None
        for k in ["embeddings", "text_embeddings", "emb", "x"]:
            if k in obj:
                emb = obj[k]
                break
        for k in ["rel_times", "times", "t_rel", "t"]:
            if k in obj:
                rel = obj[k]
                break
        if emb is None:
            # fall back to first tensor-like value
            for v in obj.values():
                if torch.is_tensor(v):
                    emb = v
                    break
        if rel is None:
            if emb is not None:
                rel = torch.arange(len(emb), dtype=torch.float32)
    elif torch.is_tensor(obj):
        emb = obj
        rel = torch.arange(len(emb), dtype=torch.float32)
    else:
        raise ValueError(f"Unsupported embedding object type in {path}: {type(obj)}")
    emb_np = emb.detach().cpu().numpy().astype(np.float32)
    rel_np = rel.detach().cpu().numpy().astype(np.float32)
    if emb_np.ndim == 1:
        emb_np = emb_np[None, :]
    if rel_np.ndim == 0:
        rel_np = np.array([float(rel_np)], dtype=np.float32)
    return emb_np, rel_np


def infer_binary_label_from_record_dir(record_dir: Path) -> int:
    """
    Modify this function if your readmission label definition differs.

    Priority:
    1) labels.json keys like readmission / readmit / label
    2) labels.csv columns like readmission / readmit / label
    3) directory name heuristics
    Default: 0
    """
    # labels.json
    json_path = record_dir / "labels.json"
    if json_path.exists():
        try:
            obj = json.loads(json_path.read_text())
            for k in ["readmission", "readmit", "label", "y"]:
                if k in obj:
                    return int(obj[k])
        except Exception:
            pass

    # labels.csv
    csv_path = record_dir / "labels.csv"
    if csv_path.exists():
        try:
            df = pd.read_csv(csv_path)
            for k in ["readmission", "readmit", "label", "y"]:
                if k in df.columns:
                    return int(df[k].iloc[0])
        except Exception:
            pass

    name = record_dir.name.lower()
    if "readmit1" in name or "positive" in name:
        return 1
    return 0


def robust_read_timeseries(ts_path: Path) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    df = pd.read_csv(ts_path)
    cols_lower = {c.lower(): c for c in df.columns}

    time_col = None
    for cand in ["time", "timestamp", "date_time", "datetime", "charttime", "hours", "t"]:
        if cand in cols_lower:
            time_col = cols_lower[cand]
            break
    if time_col is None:
        # fallback: assume row order is time
        times = np.arange(len(df), dtype=np.float32)
        val_df = df.copy()
    else:
        if np.issubdtype(df[time_col].dtype, np.number):
            times = df[time_col].to_numpy(dtype=np.float32)
        else:
            tt = pd.to_datetime(df[time_col], errors="coerce")
            base = tt.min()
            times = ((tt - base).dt.total_seconds() / 3600.0).fillna(0.0).to_numpy(dtype=np.float32)
        val_df = df.drop(columns=[time_col])

    # drop record-id like columns
    drop_cols = [c for c in val_df.columns if c.lower() in {"record_id", "hadm_id", "subject_id"}]
    val_df = val_df.drop(columns=drop_cols, errors="ignore")

    # numeric coercion
    for c in val_df.columns:
        val_df[c] = pd.to_numeric(val_df[c], errors="coerce")

    values = val_df.to_numpy(dtype=np.float32)
    mask = np.isfinite(values).astype(np.float32)
    values = np.nan_to_num(values, nan=0.0)
    return times, values, mask


def select_embedding_file(record_dir: Path, llm_model: str = "GPT2", llm_layers: int = 6) -> Optional[Path]:
    # exact expected name first
    exact = record_dir / f"text_embeddings_model={llm_model}_layers={llm_layers}_maxlen=1024.pt"
    if exact.exists():
        return exact
    # looser fallback
    cands = sorted(record_dir.glob(f"text_embeddings_model={llm_model}_layers={llm_layers}_*.pt"))
    if cands:
        return cands[0]
    cands = sorted(record_dir.glob("text_embeddings_model=*.pt"))
    return cands[0] if cands else None


class ICUReadmissionWindowDataset(Dataset):
    def __init__(
        self,
        processed_dir: Path,
        llm_model: str = "GPT2",
        llm_layers: int = 6,
        max_records: Optional[int] = None,
    ) -> None:
        self.examples: List[RecordExample] = []
        record_dirs = sorted([d for d in processed_dir.iterdir() if d.is_dir()])
        if max_records is not None:
            record_dirs = record_dirs[:max_records]
        for d in record_dirs:
            ts_path = d / "time_series.csv"
            if not ts_path.exists():
                continue
            emb_path = select_embedding_file(d, llm_model=llm_model, llm_layers=llm_layers)
            if emb_path is None:
                continue
            try:
                times, values, mask = robust_read_timeseries(ts_path)
                text_emb, text_rel = load_text_embedding_pt(emb_path)
                label = infer_binary_label_from_record_dir(d)
                self.examples.append(
                    RecordExample(
                        record_id=d.name,
                        times=times,
                        values=values,
                        mask=mask,
                        text_emb=text_emb,
                        text_rel_times=text_rel,
                        label=label,
                    )
                )
            except Exception as e:
                print(f"[WARN] skipping {d.name}: {e}")

        if len(self.examples) == 0:
            raise RuntimeError(f"No usable records found in {processed_dir}")

    def __len__(self) -> int:
        return len(self.examples)

    def __getitem__(self, idx: int) -> RecordExample:
        return self.examples[idx]


@dataclass
class BatchTensors:
    x: torch.Tensor           # [B, T, C]
    m: torch.Tensor           # [B, T, C]
    t: torch.Tensor           # [B, T]
    text_emb: torch.Tensor    # [B, N, D]
    text_t: torch.Tensor      # [B, N]
    y: torch.Tensor           # [B]
    ids: List[str]


def collate_readmission(batch: Sequence[RecordExample]) -> BatchTensors:
    B = len(batch)
    T = max(len(b.times) for b in batch)
    C = max(b.values.shape[1] for b in batch)
    N = max(b.text_emb.shape[0] for b in batch)
    D = max(b.text_emb.shape[1] for b in batch)

    x = np.zeros((B, T, C), dtype=np.float32)
    m = np.zeros((B, T, C), dtype=np.float32)
    t = np.zeros((B, T), dtype=np.float32)
    te = np.zeros((B, N, D), dtype=np.float32)
    tt = np.zeros((B, N), dtype=np.float32)
    y = np.zeros((B,), dtype=np.float32)
    ids: List[str] = []

    for i, b in enumerate(batch):
        tt_i = len(b.times)
        cc_i = b.values.shape[1]
        nn_i, dd_i = b.text_emb.shape
        x[i, :tt_i, :cc_i] = b.values
        m[i, :tt_i, :cc_i] = b.mask
        t[i, :tt_i] = b.times
        te[i, :nn_i, :dd_i] = b.text_emb
        tt[i, :nn_i] = b.text_rel_times
        y[i] = b.label
        ids.append(b.record_id)

    return BatchTensors(
        x=torch.from_numpy(x),
        m=torch.from_numpy(m),
        t=torch.from_numpy(t),
        text_emb=torch.from_numpy(te),
        text_t=torch.from_numpy(tt),
        y=torch.from_numpy(y),
        ids=ids,
    )


