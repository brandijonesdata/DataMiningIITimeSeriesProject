@dataclass
class RecordExample:
    record_id: str
    times: np.ndarray           # [T]
    values: np.ndarray          # [T, C]
    mask: np.ndarray            # [T, C]
    text_emb: np.ndarray        # [N, D] or [1, D]
    text_rel_times: np.ndarray  # [N]
