def evaluate(model, loader, device):
    model.eval()
    ys, ps = [], []

    with torch.no_grad():
        for x_ts, x_text, y in loader:
            x_ts = x_ts.to(device)
            x_text = x_text.to(device)
            y = y.to(device)

            logits = model(x_ts, x_text)
            prob = torch.sigmoid(logits)

            ys.extend(y.squeeze(-1).cpu().numpy().tolist())
            ps.extend(prob.squeeze(-1).cpu().numpy().tolist())

    ys = np.array(ys)
    ps = np.array(ps)
    pred = (ps >= 0.5).astype(int)

    return {
        "auc": safe_auc(ys, ps),
        "auprc": safe_auprc(ys, ps),
        "acc": accuracy_score(ys, pred),
        "f1": f1_score(ys, pred, zero_division=0),
        "n": len(ys),
        "pos_rate": float(np.mean(ys)),
    }

def train_model(model, train_loader, val_loader, test_loader, device, lr=1e-3, epochs=20):
    criterion = nn.BCEWithLogitsLoss()
    optimizer = torch.optim.Adam(model.parameters(), lr=lr)

    best_val_auc = -np.inf
    best_state = None

    for epoch in range(1, epochs + 1):
        model.train()
        total_loss = 0.0

        for x_ts, x_text, y in train_loader:
            x_ts = x_ts.to(device)
            x_text = x_text.to(device)
            y = y.to(device)

            optimizer.zero_grad()
            logits = model(x_ts, x_text)
            loss = criterion(logits, y)
            loss.backward()
            optimizer.step()
            total_loss += loss.item()

        val_metrics = evaluate(model, val_loader, device)
        val_auc = val_metrics["auc"]
        val_auc_for_select = -1e9 if np.isnan(val_auc) else val_auc

        if val_auc_for_select > best_val_auc:
            best_val_auc = val_auc_for_select
            best_state = {k: v.cpu().clone() for k, v in model.state_dict().items()}

        print(
            f"Epoch {epoch:02d} | "
            f"train_loss={total_loss/ max(1,len(train_loader)):.4f} | "
            f"val_auc={val_metrics['auc']:.4f} | "
            f"val_auprc={val_metrics['auprc']:.4f} | "
            f"val_acc={val_metrics['acc']:.4f} | "
            f"val_f1={val_metrics['f1']:.4f}"
        )

    if best_state is not None:
        model.load_state_dict(best_state)

    test_metrics = evaluate(model, test_loader, device)
    return model, test_metrics

