# Old-gate multimodal classifier
# --------------------------------------------------
class OldGateClassifier(nn.Module):
    def __init__(self, ts_dim, text_dim, hidden_dim=128, dropout=0.2):
        super().__init__()

        self.ts_proj = nn.Sequential(
            nn.Linear(ts_dim, hidden_dim),
            nn.ReLU(),
            nn.Dropout(dropout),
        )

        self.text_proj = nn.Sequential(
            nn.Linear(text_dim, hidden_dim),
            nn.ReLU(),
            nn.Dropout(dropout),
        )

        # old additive gate style
        self.gate = nn.Sequential(
            nn.Linear(hidden_dim * 2, hidden_dim),
            nn.Tanh(),
            nn.Linear(hidden_dim, hidden_dim),
            nn.Sigmoid(),
        )

        self.classifier = nn.Sequential(
            nn.Linear(hidden_dim, hidden_dim),
            nn.ReLU(),
            nn.Dropout(dropout),
            nn.Linear(hidden_dim, 1),
        )

    def forward(self, x_ts, x_text):
        h_ts = self.ts_proj(x_ts)
        h_text = self.text_proj(x_text)

        g = self.gate(torch.cat([h_ts, h_text], dim=-1))
        h = g * h_ts + (1.0 - g) * h_text
        logits = self.classifier(h)
        return logits

