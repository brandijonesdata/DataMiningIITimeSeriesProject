class MIMICEntityDataset(Dataset):
    def __init__(self, processed_dir, label_df):
        self.processed_dir = Path(processed_dir)
        self.label_map = dict(zip(label_df["hadm_id"].astype(str), label_df["label"].astype(float)))
        self.items = []

        for p in sorted(self.processed_dir.iterdir()):
            if not p.is_dir():
                continue
            rid = p.name
            if rid not in self.label_map:
                continue

            ts_file = p / "time_series.csv"
            text_emb_candidates = list(p.glob("text_embeddings*.pt"))

            if not ts_file.exists():
                continue
            if len(text_emb_candidates) == 0:
                continue

            self.items.append({
                "record_id": rid,
                "ts_file": ts_file,
                "text_emb_file": text_emb_candidates[0],
                "label": float(self.label_map[rid]),
            })

        print(f"Dataset size: {len(self.items)}")

    def __len__(self):
        return len(self.items)

    def __getitem__(self, idx):
        row = self.items[idx]

        df = pd.read_csv(row["ts_file"])
        # keep numeric columns only, excluding obvious IDs/time columns
        drop_cols = {"record_id", "hadm_id", "subject_id", "date_time", "time_stamp"}
        feat_cols = [c for c in df.columns if c not in drop_cols]
        X = df[feat_cols].apply(pd.to_numeric, errors="coerce").fillna(0.0).to_numpy(dtype=np.float32)

        # aggregate time series to fixed vector: mean, std, min, max over time
        x_mean = np.nanmean(X, axis=0)
        x_std = np.nanstd(X, axis=0)
        x_min = np.nanmin(X, axis=0)
        x_max = np.nanmax(X, axis=0)
        x_ts = np.concatenate([x_mean, x_std, x_min, x_max], axis=0).astype(np.float32)

        text_emb = torch.load(row["text_emb_file"], map_location="cpu")
        if isinstance(text_emb, dict):
            # try common keys
            for k in ["embedding", "embeddings", "text_embedding", "hidden_states"]:
                if k in text_emb:
                    text_emb = text_emb[k]
                    break
        if isinstance(text_emb, list):
            text_emb = np.array(text_emb, dtype=np.float32)
        if torch.is_tensor(text_emb):
            text_emb = text_emb.detach().cpu().float().numpy()

        text_emb = np.asarray(text_emb, dtype=np.float32)
        if text_emb.ndim == 1:
            x_text = text_emb
        else:
            x_text = np.mean(text_emb, axis=0)

        y = np.float32(row["label"])

        return (
            torch.tensor(x_ts, dtype=torch.float32),
            torch.tensor(x_text, dtype=torch.float32),
            torch.tensor([y], dtype=torch.float32),
        )

def collate_fn(batch):
    xs, xt, y = zip(*batch)
    xs = torch.stack(xs, dim=0)
    xt = torch.stack(xt, dim=0)
    y = torch.stack(y, dim=0)
    return xs, xt, y

