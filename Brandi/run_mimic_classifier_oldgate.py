def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--task", type=str, required=True, choices=["mortality", "icu_readmit"])
    parser.add_argument("--mimic_root", type=str, required=True)
    parser.add_argument("--batch_size", type=int, default=16)
    parser.add_argument("--epochs", type=int, default=20)
    parser.add_argument("--seed", type=int, default=1)
    args = parser.parse_args()

    set_seed(args.seed)
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print("Device:", device)

    if args.task == "mortality":
        label_df = build_mortality_labels(args.mimic_root)
    else:
        label_df = build_icu_readmit_labels(args.mimic_root)

    LABEL_DIR.mkdir(parents=True, exist_ok=True)
    out_label_path = LABEL_DIR / f"{args.task}_labels_from_runner.csv"
    label_df.to_csv(out_label_path, index=False)
    print("Saved labels to:", out_label_path)
    print(label_df["label"].value_counts(dropna=False))

    ds = MIMICEntityDataset(PROCESSED_DIR, label_df)

    n = len(ds)
    n_train = int(0.7 * n)
    n_val = int(0.15 * n)
    n_test = n - n_train - n_val

    train_ds, val_ds, test_ds = torch.utils.data.random_split(
        ds, [n_train, n_val, n_test],
        generator=torch.Generator().manual_seed(args.seed)
    )

    train_loader = DataLoader(train_ds, batch_size=args.batch_size, shuffle=True, collate_fn=collate_fn)
    val_loader = DataLoader(val_ds, batch_size=args.batch_size, shuffle=False, collate_fn=collate_fn)
    test_loader = DataLoader(test_ds, batch_size=args.batch_size, shuffle=False, collate_fn=collate_fn)

    sample_ts, sample_text, _ = ds[0]
    model = OldGateClassifier(
        ts_dim=sample_ts.shape[0],
        text_dim=sample_text.shape[0],
        hidden_dim=128,
        dropout=0.2,
    ).to(device)

    model, test_metrics = train_model(
        model, train_loader, val_loader, test_loader, device,
        lr=1e-3, epochs=args.epochs
    )

    print("\nFinal test metrics")
    for k, v in test_metrics.items():
        print(f"{k}: {v}")

    save_dir = REPO_ROOT / "run_logs"
    save_dir.mkdir(parents=True, exist_ok=True)
    torch.save(model.state_dict(), save_dir / f"oldgate_{args.task}_classifier.pt")
    print("Saved model to:", save_dir / f"oldgate_{args.task}_classifier.pt")

if __name__ == "__main__":
    main()
